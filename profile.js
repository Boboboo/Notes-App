// Ajax call to updateusername.php
$("#updateusernameform").submit(function(event){ 
    //prevent default php processing
    event.preventDefault();
    //collect user inputs
    var datatopost = $(this).serializeArray();
//    console.log(datatopost);
    //send them to updateusername.php using AJAX
    $.ajax({
        url: "updateusername.php",
        type: "POST",
        data: datatopost,
        success: function(data){
            if(data){
                $("#updateusernamemessage").html(data);
            }else{
                location.reload();       //这边reload page是因为上面有一个显示user_name，当你更改user_name后要使那边也更新
            }
        },
        error: function(){
            $("#updateusernamemessage").html("<div class='alert alert-warning'>There was an error with the Ajax Call. Please try again later.</div>");     
        }  
    });
});

